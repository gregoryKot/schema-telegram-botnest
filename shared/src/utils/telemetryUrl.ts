/**
 * Безопасный URL для краш-телеметрии: только схема+хост+путь.
 *
 * Во фрагменте URL живут ЖИВЫЕ креденшелы:
 *   • мини-апп грузится с `#tgWebAppData=…&hash=<подпись>` — это валидная
 *     initData; окно жизни 1 час, реплей = полная имперсонация пользователя
 *     (чтение и запись всех клинических данных);
 *   • вебапп после логина попадает на `/auth/callback#access_token=<JWT>`
 *     (TTL 15 минут) — этого хватает на захват аккаунта.
 *
 * Раньше в краш-репорт клался `location.href` целиком, а бэкенд логировал его
 * через `.error()` → stdout + DM админу. То есть логи и личка админа
 * становились хранилищем живых креденшелов (аудит 2026-07-20, H0).
 *
 * Путь без query/fragment по-прежнему отвечает на главный вопрос телеметрии —
 * «какой экран упал», — но не несёт секретов.
 */
export function telemetryUrl(
  href: string | undefined | null,
): string | undefined {
  if (!href) return undefined;
  // Всё от первого '#' или '?' — потенциальный секрет, режем безусловно.
  const withoutSecrets = href.split('#')[0].split('?')[0].trim();
  return withoutSecrets ? withoutSecrets.slice(0, 200) : undefined;
}
