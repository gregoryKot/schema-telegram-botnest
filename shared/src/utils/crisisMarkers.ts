// Кризисная детекция (аудит 2026-07, правило №7 CLAUDE.md: свободный текст =
// кризисный путь). Работает ТОЛЬКО на клиенте, до шифрования и отправки:
// сервер расшифрованный текст не видит, карточка никуда ничего не передаёт.
//
// ВАЖНО: файл парный (webapp ↔ schema-miniapp) — правки вносить в оба.
// Полнота важнее точности: ложная карточка — это просто ненавязчивая ссылка
// на телефон доверия (дёшево), а пропуск реального кризисного текста —
// катастрофа. Паттерны сознательно расширяются, но остаются словарными
// («убить время», «умираю со смеху» — не должны срабатывать).

const CRISIS_PATTERNS: RegExp[] = [
  /суицид/,
  /самоубийств/,
  /(убить|убью)\s+себя/,
  /покончи(ть|у|л|ла)\s+с\s+собой/,
  /не\s*хочу\s+(больше\s+)?жить/,
  /не\s*хочется\s+жить/,
  /жить\s+не\s+хочу/,
  /(незачем|не\s*за\s*чем)\s+жить/,
  /хочу\s+умереть/,
  /умереть\s+хочу/,
  /лучше\s+умереть/,
  /повес(иться|ился|илась)|повешусь/,
  /вскр(ыть|ою)\s+(себе\s+)?вены/,
  /(порезать|порежу)\s+(себе\s+)?вены/,
  /наглота(ться|юсь)\s+таблеток/,
  /самоповрежд/,
  /селфхарм/,
  /self[-\s]?harm/,
  /реж(у|ешь)\s+себя/,
  /порезать\s+себя/,
  /спрыгн(уть|у)\s+с\s+(крыши|моста|окна)/,
  /(выброс(иться|ишься|ился|илась)|выброшусь)\s+из\s+окна/,
  /хочу\s+исчезнуть/,
  // Жаргонный эвфемизм суицида. Только возвратные формы («-ся/-юсь») —
  // «выпилить дерево» (бытовое, без возврата к себе) специально не ловим.
  /выпил(иться|юсь|ился|илась)/,
];

// Латинские буквы, визуально неотличимые от кириллических (гомоглифы),
// которыми иногда «маскируют» кризисное слово (напр. «cуицид» с латинской c).
const LATIN_HOMOGLYPHS: Record<string, string> = {
  a: 'а',
  c: 'с',
  e: 'е',
  o: 'о',
  p: 'р',
  x: 'х',
  y: 'у',
  k: 'к',
  m: 'м',
  t: 'т',
  b: 'в',
  h: 'н',
};

function normalize(text: string): string {
  return text.toLowerCase().replace(/ё/g, 'е');
}

/**
 * Заменяет латинские гомоглифы на кириллические аналоги. Применяется ОТДЕЛЬНО
 * от обычной нормализации: если гонять по ней и англоязычные паттерны
 * (self-harm), буквы e/a/h/m в английских словах превратятся в кириллицу и
 * сломают матч — поэтому detectCrisis проверяет оба варианта строки.
 */
function toHomoglyphNormalized(text: string): string {
  return text.replace(/[a-z]/g, (ch) => LATIN_HOMOGLYPHS[ch] ?? ch);
}

export function detectCrisis(text: string | null | undefined): boolean {
  if (!text) return false;
  const normalized = normalize(text);
  const homoglyphNormalized = toHomoglyphNormalized(normalized);
  return CRISIS_PATTERNS.some(
    (re) => re.test(normalized) || re.test(homoglyphNormalized),
  );
}

/** Проверка сразу нескольких полей формы одной строкой. */
export function detectCrisisAny(
  ...texts: Array<string | null | undefined>
): boolean {
  return texts.some(detectCrisis);
}

