const WEEKDAYS = [
  'воскресенье',
  'понедельник',
  'вторник',
  'среда',
  'четверг',
  'пятница',
  'суббота',
];
const SEASONS = ['зима', 'весна', 'лето', 'осень'];

/** Контекст для генерации: день недели, сезон и последние посты. */
export function buildHealthyAdultUserPrompt(
  recentPosts: string[],
  now = new Date(),
): string {
  // По московскому времени — канал живёт в этом поясе.
  const msk = new Date(
    now.toLocaleString('en-US', { timeZone: 'Europe/Moscow' }),
  );
  const weekday = WEEKDAYS[msk.getDay()];
  const season = SEASONS[Math.floor(((msk.getMonth() + 1) % 12) / 3)];
  const recent =
    recentPosts.length > 0
      ? recentPosts.map((t, i) => `${i + 1}. ${t}`).join('\n')
      : '(пока пусто)';
  return `Сегодня ${weekday}, время года — ${season}.

Последние опубликованные сообщения (НЕ повторяй их форму, стартовые конструкции и ключевые слова):
${recent}

Напиши одно новое сообщение.`;
}
