/** Сколько постов в день (утро + вечер). Определяет шаг ротации фраз. */
export const HEALTHY_ADULT_SLOTS_PER_DAY = 2;

/**
 * Детерминированный индекс фразы по дню и слоту. Последовательный обход всего
 * пула гарантирует, что фраза не повторится, пока не пройдёт весь список.
 * Чистая функция — без состояния и БД, тестируется напрямую.
 */
export function healthyAdultIndex(
  dayEpoch: number,
  slot: number,
  poolSize: number,
): number {
  const seq = dayEpoch * HEALTHY_ADULT_SLOTS_PER_DAY + slot;
  return ((seq % poolSize) + poolSize) % poolSize;
}

/** Фраза для конкретной даты и слота (0 = утро, 1 = вечер). */
export function pickHealthyAdultPhrase(
  date: Date,
  slot: number,
  pool: readonly string[] = HEALTHY_ADULT_PHRASES,
): string {
  const dayEpoch = Math.floor(date.getTime() / 86_400_000);
  return pool[healthyAdultIndex(dayEpoch, slot, pool.length)];
}
