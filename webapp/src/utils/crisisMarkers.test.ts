// Тест кризисной детекции (аудит 2026-07, этап 0.2 REMEDIATION_PLAN).
// Точность важнее полноты: ложная карточка в личном дневнике подрывает
// доверие, поэтому негативные кейсы («убить время») здесь не менее важны.
import { describe, it, expect } from 'vitest';
import { detectCrisis, detectCrisisAny } from './crisisMarkers';

describe('detectCrisis', () => {
  it.each([
    'думаю о суициде',
    'мысли о самоубийстве не уходят',
    'хочу убить себя',
    'я убью себя если это повторится',
    'хочется покончить с собой',
    'я больше не хочу жить',
    'не хочу   жить',
    'жить не хочу совсем',
    'незачем жить',
    'просто хочу умереть',
    'лучше умереть чем так',
    'думала повеситься',
    'хотел вскрыть вены',
    'наглотаюсь таблеток и всё',
    'опять занимаюсь самоповреждением',
    'селфхарм вернулся',
    'self-harm опять',
    'вчера снова режу себя',
    'хочу спрыгнуть с крыши',
    'хочу исчезнуть',
  ])('срабатывает на «%s»', (text) => {
    expect(detectCrisis(text)).toBe(true);
  });

  it('не зависит от регистра и ё/е', () => {
    expect(detectCrisis('НЕ ХОЧУ ЖИТЬ')).toBe(true);
    expect(detectCrisis('хочется покончить с собоЙ')).toBe(true);
  });

  it.each([
    'сегодня хороший день, гулял в парке',
    'хочу убить время до встречи',
    'умираю со смеху над этим сериалом',
    'жизнь налаживается',
    'порезал хлеб и приготовил ужин',
  ])('НЕ срабатывает на «%s»', (text) => {
    expect(detectCrisis(text)).toBe(false);
  });

  it('пустые значения — false', () => {
    expect(detectCrisis('')).toBe(false);
    expect(detectCrisis(null)).toBe(false);
    expect(detectCrisis(undefined)).toBe(false);
  });

  // Доп. инфлексии и ветки регексов, которые не были задействованы исходным
  // набором (напр. «лась»-форма выброситься вообще не проверялась ни разу).
  it.each([
    'не хочу больше жить, сил нет', // опциональная группа «больше» между хочу/жить
    'совсем не хочется жить', // ветка «не хочется жить» отдельно от «не хочу»
    'не за чем жить дальше', // альтернативное раздельное написание «не за чем»
    'умереть хочу прямо сейчас', // обратный порядок «умереть хочу»
    'вскрою себе вены сегодня же', // ветка «ою» + опциональное «себе»
    'хочу вскрыть себе вены', // ветка «ыть» вместе с «себе» (в базовых тестах без «себе»)
    'спрыгну с моста, надоело всё', // ветка «спрыгн+у» + локация «моста»
    'выбросилась из окна подруга, и теперь я тоже об этом думаю', // ветка «лась», раньше не покрыта
    'хочу наглотаться таблеток снова', // ветка «ться» (базовый тест проверял только «юсь»)
    'self harm continues', // разделитель — пробел (базовый тест проверял дефис)
    'пыталась покончить с собой в прошлом году, и снова накатывает', // рассказ о прошлой попытке — тоже должен сработать
  ])('срабатывает на «%s»', (text) => {
    expect(detectCrisis(text)).toBe(true);
  });

  // Идиомы и нейтральные упоминания смерти/тела, которые не должны поднимать
  // тревогу — ложная карточка в дневнике так же вредна, как пропуск.
  it.each([
    'страх смерти преследует меня с детства',
    'боюсь умереть во сне',
    'вены на руке проступают, когда мерзну',
    'умираю от скуки на этой встрече',
    'хочу выпилить старое дерево в саду', // созвучно эвфемизму «выпилиться», но обычная бытовая фраза
  ])('НЕ срабатывает на «%s»', (text) => {
    expect(detectCrisis(text)).toBe(false);
  });
});

describe('detectCrisisAny', () => {
  it('находит маркер в любом из полей формы', () => {
    expect(detectCrisisAny('обычный текст', '', 'не хочу жить')).toBe(true);
    expect(detectCrisisAny('обычный текст', null, undefined)).toBe(false);
  });
});

// Известные ограничения детекции — не todo и не skip: это фиксация ТЕКУЩЕГО
// поведения регексов, чтобы файл упал, если поведение случайно изменится
// (например, кто-то «улучшит» паттерн и тихо расширит/сузит покрытие).
// Каждый кейс ниже — подтверждённый пропуск (false negative), проверенный
// напрямую по regex из crisisMarkers.ts. Источник правды на фикс — само
// исходное правило (см. правило №7 CLAUDE.md); файл сознательно не трогаем,
// т.к. это парный файл (webapp ↔ schema-miniapp).
describe('известные ограничения детекции (документация)', () => {
  it('слитное написание — «нехочу жить» не ловится', () => {
    // Паттерн требует «не\s+хочу», пробела между «не» и «хочу» нет — миссится.
    expect(detectCrisis('нехочу жить')).toBe(false);
  });

  it('опечатка «жыть» вместо «жить» не ловится', () => {
    // Нормализация приводит только ё→е, опечатки в корне не исправляются.
    expect(detectCrisis('жыть больше не хочу')).toBe(false);
  });

  it('прошедшее время: спрягаемая форма «покончила» не ловится', () => {
    // Паттерн — /покончи(ть|у)\s+с\s+собой/: ловит инфинитив «покончить»
    // (и «пыталась покончить с собой» — см. позитивный кейс выше, там
    // инфинитив внутри фразы), но НЕ ловит спрягаемые прошедшие формы
    // «покончила»/«покончил», где после «покончи» идёт «ла»/«л», а не «ть»/«у».
    expect(detectCrisis('покончила с собой')).toBe(false);
  });

  it('латиница-микс: латинская «c» в «cуицид» не ловится', () => {
    // Первая буква ниже — латинская «c» (U+0063), а не кириллическая «с»
    // (U+0441): визуально неотличимы, но /суицид/ требует кириллицу.
    expect(detectCrisis('cуицид не отпускает')).toBe(false);
  });

  it('эвфемизм «выпилиться» не ловится', () => {
    // Список паттернов не включает жаргонные эвфемизмы суицида.
    expect(detectCrisis('хочу выпилиться')).toBe(false);
  });

  it('1 лицо будущего времени «повешусь» не ловится — опасный пропуск', () => {
    // Паттерн /повес(иться|люсь)/ ловит «повеситься» (инфинитив), но ветка
    // «люсь» не соответствует реальной русской форме — грамотное «повешусь»
    // (наиболее естественная фраза о собственном намерении) не совпадает.
    expect(detectCrisis('повешусь если так будет продолжаться')).toBe(false);
  });

  it('1 лицо будущего времени «выброшусь из окна» не ловится — опасный пропуск', () => {
    // Паттерн покрывает «выброситься/выбросишься/выбросился/выбросилась», но
    // не «выброшусь» — самую естественную формулировку намерения от первого лица.
    expect(detectCrisis('выброшусь из окна')).toBe(false);
  });

  it('1 лицо будущего времени «порежу себе вены» не ловится — опасный пропуск', () => {
    // Паттерн /порез(ать|жу)\s+.../ рассчитан на «порезжу», а не на реальную
    // форму «порежу» (чередование з→ж) — фраза о намерении не совпадает
    // даже с явным «себе вены».
    expect(detectCrisis('порежу себе вены')).toBe(false);
  });
});
