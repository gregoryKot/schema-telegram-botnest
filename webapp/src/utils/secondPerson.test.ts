import { describe, it, expect } from 'vitest';
import { hasSecondPerson } from './secondPerson';

describe('hasSecondPerson', () => {
  it.each([
    'Ты справишься',
    'это твой выбор',
    'вы не одни',
    'берегите вас самих',
    'ты делаешь достаточно', // глагол 2 л. ед.ч. на -ешь
    'подумайте об этом', // императив на -йте
  ])('ловит обращение: %s', (t) => {
    expect(hasSecondPerson(t)).toBe(true);
  });

  it.each([
    'Чувства — это информация, а не приговор.',
    'Отдых — базовая потребность.',
    'Я имею право сказать «нет».',
    'Можно остановиться и просто подышать.',
    'Строгость к себе не делает сильнее — она истощает.',
  ])('пропускает безличное/«я»: %s', (t) => {
    expect(hasSecondPerson(t)).toBe(false);
  });
});
