// Кризисная детекция (аудит 2026-07, правило №7 CLAUDE.md: свободный текст =
// кризисный путь). Работает ТОЛЬКО на клиенте, до шифрования и отправки:
// сервер расшифрованный текст не видит, карточка никуда ничего не передаёт.
//
// ВАЖНО: файл парный (webapp ↔ schema-miniapp) — правки вносить в оба.
// Паттерны сознательно узкие (высокая точность, без срабатываний на
// «убить время» или «умираю со смеху»): ложная карточка в дневнике
// подрывает доверие сильнее, чем пропуск.

const CRISIS_PATTERNS: RegExp[] = [
  /суицид/,
  /самоубийств/,
  /(убить|убью)\s+себя/,
  /покончи(ть|у)\s+с\s+собой/,
  /не\s+хочу\s+(больше\s+)?жить/,
  /не\s+хочется\s+жить/,
  /жить\s+не\s+хочу/,
  /(незачем|не\s+за\s+чем)\s+жить/,
  /хочу\s+умереть/,
  /умереть\s+хочу/,
  /лучше\s+умереть/,
  /повес(иться|люсь)/,
  /вскр(ыть|ою)\s+(себе\s+)?вены/,
  /порез(ать|жу)\s+(себе\s+)?вены/,
  /наглота(ться|юсь)\s+таблеток/,
  /самоповрежд/,
  /селфхарм/,
  /self[-\s]?harm/,
  /реж(у|ешь)\s+себя/,
  /порезать\s+себя/,
  /спрыгн(уть|у)\s+с\s+(крыши|моста|окна)/,
  /выброси(ться|шься|лся|лась)\s+из\s+окна/,
  /хочу\s+исчезнуть/,
];

function normalize(text: string): string {
  return text.toLowerCase().replace(/ё/g, 'е');
}

export function detectCrisis(text: string | null | undefined): boolean {
  if (!text) return false;
  const t = normalize(text);
  return CRISIS_PATTERNS.some((re) => re.test(t));
}

/** Проверка сразу нескольких полей формы одной строкой. */
export function detectCrisisAny(...texts: Array<string | null | undefined>): boolean {
  return texts.some(detectCrisis);
}

